    private void reserveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reserveActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = DBAccess.getConnection();
            PreparedStatement stmnt;
            if (null == conn) {
                JOptionPane.showMessageDialog(rootPane, "Connection failed");
            } else {
                String sqlCmd = "update venue set booked=1 where name=?";
                stmnt = conn.prepareStatement(sqlCmd);
                stmnt.setString(1, venue.getSelectedItem().toString());
                int result = stmnt.executeUpdate();
                if (result > 0) {
                    JOptionPane.showMessageDialog(rootPane, "Successfully booked");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Not booked");
                }
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(LoginAdmin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(LoginAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_reserveActionPerformed

