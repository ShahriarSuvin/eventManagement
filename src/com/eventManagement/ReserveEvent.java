    private void getQuoteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getQuoteActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = DBAccess.getConnection();
            PreparedStatement stmnt;
            if (null == conn) {
                JOptionPane.showMessageDialog(rootPane, "Connection failed");
            } else {
                String sqlCmd = "SELECT * FROM `venue` where name=?";
                stmnt = conn.prepareStatement(sqlCmd);
                stmnt.setString(1, venue.getSelectedItem().toString());
                ResultSet result = stmnt.executeQuery();
                if (result.next() && Integer.parseInt(attendee.getText()) <= result.getInt("capacity")) {
                    foodPrice.setText(Integer.toString(Integer.parseInt(attendee.getText()) * result.getInt("food_price")));
                    venueFare.setText(Integer.toString(result.getInt("price")));
                    total.setText(Integer.toString(result.getInt("price") + Integer.parseInt(attendee.getText()) * result.getInt("food_price")));
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Venue capacity exceeded!");
                    foodPrice.setText("Venue capacity exceeded!");
                }
                
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(LoginAdmin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(LoginAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_getQuoteActionPerformed

